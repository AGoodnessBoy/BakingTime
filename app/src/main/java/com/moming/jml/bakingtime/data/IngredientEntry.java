public class IngredientEntry {
    String ingr_name;
    String ingr_quantity;
    String ingr_measure;

    public String getIngr_name() {
        return ingr_name;
    }

    public void setIngr_name(String ingr_name) {
        this.ingr_name = ingr_name;
    }

    public String getIngr_quantity() {
        return ingr_quantity;
    }

    public void setIngr_quantity(String ingr_quantity) {
        this.ingr_quantity = ingr_quantity;
    }

    public String getIngr_measure() {
        return ingr_measure;
    }

    public void setIngr_measure(String ingr_measure) {
        this.ingr_measure = ingr_measure;
    }
}
